// [1.0] Destructuring easily extract array elements or object properties and store them in variables.
// [1.1] Array Destructuring
[a, b] = ['Hello', 'nerdrobot'];
console.log(a); // Hello
console.log(b); // nerdrobot

// [1.1] Object  Destructuring
let {name} = {name: 'nerdrobot', age: 99};
console.log(name); // nerdrobot
console.log(age); // undefined
